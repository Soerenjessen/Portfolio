           <p class="tekst"> Da vi havde været igennem den indledende proces (Empathize/Define fase), begyndte vi at skitsere, hvordan Det bette Ismejeris hjemmeside kunne se ud. Vi startede med at tegne nogle wireframes, som skulle give et overordnet billede på, hvordan siden kunne se ud. </p> 
            
