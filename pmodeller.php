            <h1> 5 grundantagelser om kommunikation </h1> 
            
            <p class="tekst"> 1. Man kan ikke undgå at kommunikere. Alt hvad vi gør, er kommunikation.</p> 
            
            <p class="tekst"> 2. Enhver kommunikation består af to dele. Selve  indholdet og oplysninger om det indbyrdes forhold, der er mellem   dem, der kommunikerer(Daugaard, 2018, S.9-10 ,Kommunikation MMD1 11092018 kommunikationsteori). 
            Indholdet kan forstås på forskellige måder. Det handler om, hvordan vi perciperer hinanden. Hvor godt kender vi hinanden, og hvad er vores indbyrdes forhold.
            </p> 
                
            <p class="tekst">3.	Den måde, vi udtrykker os på, og den måde, vi tolker hinandens kommunikation på, afhænger også af det forhold vi har til hinanden.(Daugaard, 2018, S.9-10 ,Kommunikation MMD1 11092018 kommunikationsteori).    
            Den handler om den måde vi kommunikerer på i forhold til pauser og betoning.
            </p>  
            
            <p class="tekst">4.	Mennesker kommunikerer både med en digital kode og en analog kode.(Daugaard, 2018, S.9-10 ,Kommunikation MMD1 11092018 kommunikationsteori).  
            Digital kode: hvad der siges (ord)
            Analog kode: hvordan det siges (tonefald, kropsprog, skrifttype/opsætning).
            </p>
            
            <p class="tekst">5.	Kommunikationsudveksling er enten symmetrisk eller asymmetrisk afhængigt af, om den hviler på lighed eller forskel i magt. 
            Når man kommunikerer symmetrisk, kommunikerer man på samme niveau. 
            Ex: To hjertelæger har en dialog om kardiologi. 
            Når man kommunikerer asymmetrisk kommunikere man ikke på samme niveau. Ex forældre taler til sit barn. 
            </p>
            
