            <h2> Synkron og Asynkron kommunikation handler om kommunikation i forhold til tid. </h2> 
            
            <p class="tekst"> -	Synkron kommunikation, er når de              kommunikerende enten skriver eller taler på samme tid. Et eksempel kunne være en dialog, hvor man sidder fysisk over for hinanden. 
            Eller en chat, hvor begge er online samtidig.  
            </p> 
                
            <p class="tekst">-	Asynkron kommunikation betyder, at der er en tidsforskydning i forhold til kommunikationen. Det kunne f.eks. være et gammeldags Postkort, som typisk er flere dage undervejs. </p>  
            
