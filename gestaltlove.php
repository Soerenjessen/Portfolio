            <h2> Simplicity </h2> 
            
            <p class="tekst"> Når man arbejder med simplicity handler det om at skabe nytte. Man skal fjerne alt, som kan distrahere brugeren, og ingen funktion har, men blot er med for at pynte. </p>
            
            <p class="tekst"> I forhold til en hjemmeside handler det om at designe siden, så det er nemt for brugerne at finde rundt på siden og finde det væsentlige. Siden skal altså være nem at bruge. </p>
            
            <p class="tekst"> Man kan opnå simplicity ved at arbejde med farver, skrifttyper og grafiske løsninger. </p>
            
            <p class="tekst"> -	Farver: Brug max. 5 forskellige farver. </p>
            
            <p class="tekst"> -	Skrifttyper: Brug højst 2 forskellige skrifttyper i max. 3 størrelser. (Sans Serif til overskrifter og serif til brødtekst). </p>
            
            <p class="tekst">  - Grafiske løsninger: Bør ikke bruges til at pynte med, men derimod til at gøre det nemmere for brugeren – Brug ikoner og symboler til at navigere i indholdet.</p>
            
            
