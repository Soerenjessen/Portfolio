            <p class="tekst">I denne periode opstår nye forskellige sociale medier, som f.eks. MySpace og Facebook.</p> 
            
            <p class="tekst"> En af de første sociale medier er mediet MySpace, som har fokus på musik. MySpace bliver populært, da musikere og kunstnere nu selv kan udgive deres egen musik uden om pladeselskaber, hvilket revolutionerer hele musikbranchen på meget få år. MySpace bliver et forum, hvor folk kan finde nichemusik, som ikke bliver spillet i mainstream radio og tv. Dette tiltrækker store grupper af mennesker, som interesser sig for f.eks. Metal, Rap, Hiphop og mange andre genrer, som på dette tidspunkt var nichegenre. </p> 
            
