            <h2>Tidlige Eksperimenter (1993-1995)</h2>
            
            <p class="tekst"> I 1993 begynder udviklingen af den første brugbare browser NCSA Mosaic, som er med til at sætte gang i videreudviklingen af webbet.</p> 
            
            <p class="tekst"> Det der kendetegner denne periode er, at forskellige individer, virksomheder og organisationer eksperimenter med, hvordan webbet skal se ud. Der er ikke nogen officiel syntaks, eller guideline til, hvordan webbet skal se ud. Denne periode er en form for det vilde vesten.</p> 
        
            <p class="tekst">I denne periode var HTML-sproget og browserne teknisk begrænsende i forhold til design og layout. Websiderne var opbygget af simpel hypertekst, uden fokus på design. Stilarten på mange af siderne var det klassiske bogformat. Dette var ikke ligefrem med til at øge brugervenligheden på siderne.</p> 
             
            <p class="tekst">Det er generelt en periode, hvor der er meget uvished om hvilken retning webbet skal udvikle sig. I994 udkommer Netscape 1.1, hvor det var muligt at arbejde med HTML tabeller, og det blev muligt at indsætte baggrundsbilleder ved hjælp af GIF og JPEG, hvilket gav helt nye designmuligheder.</p> 
                      
