            <h2>Hvad kendetegner udviklingen af internettet?</h2>
            
            <p class="tekst"> Protokoller har været afgørende for internettet. Det er et regelsæt (en aftale), som gør, at vi kommunikerer på det samme sprog. </p> 
                
            <h2>Hvorfra kommer begrebet "Inter-net" og hvad hentyder det til?</h2>
            
            <p class="tekst">Begrebet stammer fra den Franske afdeling Cyclades – På grund af manglende budget, ville man have maskiner til at kommunikere sammen = Deraf navnet Inter-net.</p> 
            
            <h2>Hvornår startede udviklingen af internettet?</h2>
            
            <p class="tekst">Udviklingen af internettet startede tilbage i 1957. Det startede under den kolde krig, da Sovjetunionen, som den første nation i verden, sendte den ubemandede rumsonde Sputnik i kredsløb rundt om jorden. I 1966 begyndte man arbejdet på ARPANET, som var et samarbejde imellem lande som England, USA og Frankring. De forskellige interesser heri var militære, videnskabelige og kommercielle</p>
            
            <h2> Hvad er "information overload"</h2>
            
            <p class="tekst">Information overload opstod omkring starten af år 1900. Mængden af viden der skulle håndteres, var ganske enkelt blevet uoverskuelig. Man ønskede at organisere viden, og gøre den tilgængelig for alle.</p>
            
            <h2> Hvad var ideen bag "World Palace of Knowledge"</h2>
            
            <p class="tekst">Folkene bag World Palace of Knowledge var Paul Otlet og Henri LaFontaine, der havde en vision om, at verdensfred kunne opnås ved global vidensdeling. De begyndte at samle og organisere viden, i verdens første manuelle søgemaskine. 

            De skabte i bund og grund et kæmpe arkiv med over 50 millioner dokumenter og videofilm. Når almindelige mennesker sendte spørgsmål til World Palace of Knowledge, var der ansatte, som fandt alt information der var på området, men det helt unikke var, at de også fandt relateret artikler til det specifikke emne, altså en form for linking. 
            </p>
            
            <h2>Hvad var ideen bag "The Memex"?</h2>
            
            <p class="tekst">
            Det samme princip som ”World Palace of Knowledge” men mere automatisk. Brug af keywords, for at finde de rigtige informationer, det gjorde man ved at benytte association (Linking). 
            </p>    
            
            <h2>Hvem var Douglas Engelbart og hvad er han kendt for?</h2>
            
            <p class="tekst">Pioneer indenfor udviklingen af computeren tilbage i slutningen af 60erne. Hans forskerteam opfandt bl.a. ”The Mouse” musen. De forsøgte at lave værktøjerne til i praksis, at kunne dele information.</p> 
            
            <h2>Hvem opfandt begrebet "Hypertext" og hvad kunne det første Hypertext System.</h2>
            
            <p class="tekst">-	Ted Nelson – Man kunne skrive ord og åbne flere vinduer. Han anså alt tekst som forbundet. Deraf navnet Hypertext. </p> 
            
            <h2>Hvordan inspirerede Tim's far's artikel Tim?</h2>
            
            <p class="tekst">Den inspirerede ham til, hvordan en computer kan etablere links ligesom den menneskelige hjerne gør. Hjernen linker ting som har en fælles funktion, selvom de i første omgang kan virke fuldstændig forskellige. F.eks. en hest - Det kan være en hest på en mark, det kan være en hest på et skakbræt, det kan være en hest i en gymnastiksal. 
            Det var denne sammenhæng mellem tekst, som man kalder hypertekst, der inspirerede Tim Berners Lee. Han arbejdede med ideen om at lære computeren at læse hypertekst. 
            </p> 
            
            <h2>Hvad var programmet Enquire</h2>
            
            <p class="tekst">Da Tim Berners Lee kom til Cern i 1980, ville han gerne have et overblik over, hvem der stod for hvad, og hvilke computerprogrammer de brugte. For at få et overblik skabte han Enquire, som var et program der kunne sammenligne dokumenter omkring ansatte, deres projekter, og den software de brugte. Enquire kunne tjekke om dokumenterne indeholdte nogle specifikke keywords. Hvis det gjorde, var deres lokation i dokumentet inter linked.</p> 
            
            <h2>Hvad var problemet med ARPANET?</h2>
            
            <p class="tekst">Problemet med Arpanet var, at det blev brugt af forskellige typer computere. Dem som arbejdede med en slags computer, havde brug for en grundlæggende viden om andre computere for at kunne genkende data herfra, da computere havde forskellige styresystemer, kunne de ikke forstå hinanden. </p> 
            
            <h2>Hvad var Tim's ide?</h2>
            
            <p class="tekst">Tim Berners Lees ide var at udvikle et fælles computersprog, så alle brugere på netværket kunne læse hinandens data, uafhængigt af hvilket computersystem de brugte. </p> 
            
            <h2>Hvad står HTML og URL for?</h2>
            
            <p class="tekst">HTML står for Hyper Text Markup Language og URL står for Universal Ressource Locator.</p> 
            
            <h2> Hvad er en Browser?</h2>
            
            <p class="tekst">En browser er et program, som viser et billede af HTML dokumenter på en computer. </p> 
            
            <h2>Hvem var Marc Andreasen?</h2>
            
            <p class="tekst">Marc Andreasen var en ung studerende, som skabte browseren Mosaic. Mosaic indeholdte både hyperlinks og billeder. </p> 
            
