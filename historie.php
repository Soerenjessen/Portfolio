            <h2>Under Konstruktion (1996-2000)</h2>
            
            <p class="tekst">I denne periode opstår webguruernes kamp. </p> 
            
            <p class="tekst"> På den ene side var David Siegel, som var meget visuelt orienteret. Han fokuserede meget på layout og design, og havde en meget kreativ tankegang om, hvordan websites kunne tage sig ud. David Siegel havde meget fokus på design og avancerede funktioner, som skulle gøre webbet et mere interessant sted at befinde sig. </p> 
        
            <p class="tekst">På den anden side var danske Jacob Nielsen, som havde en anden filosofi omkring webbet. Jacob Nielsens fokus var på brugervenlighed og funktion. I løbet af 1990erne opnår han status som webguru, og udgiver forskellige videnskabelige bøger omkring usability. I forhold til design, havde han en mere minimalistisk tilgang end David Siegel. I dag vil man nok mene, at Jacob Nielsen havde en meget firkantet tankegang omkring webudviklingen, da han satte brugervenlig før design. </p> 
             
            <p class="tekst">Hvorimod David Siegel forsøgte at forene design, teknik og indhold på websider. Til trods må man den dag i dag, stadig anerkende mange af Jacob Nielsens tanker og anskuelser. </p> 
                      
            <p class="tekst">I samme periode kører browser-krigen, som er en kamp imellem forskellige browserudviklere. I denne periode konkurrerede Netscape med Microsofts, Internet Explorer, om at vinde webmarkedet. Her havde Microsoft den fordel, at de installerede Internet Explorer på deres egne computere, hvilket sikrede dem stort set hele markedet.  </p> 
            
            <p class="tekst">Derudover var Internet Explorer den første browser, som understøttede fremtidens styllingsprog Cascading Style Sheet, bedre kendt som CSS.</p> 
            
