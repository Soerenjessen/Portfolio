            <h2>Kvalitativ metode</h2> 
            
            <p class="tekst"> I den kvalitative metode arbejder man med data, som ikke kan beskrives i konkrete talstørrelser f.eks. holdninger, adfærd osv. Det er indsamling af bløde data.  Den kvalitative metode handler om at forstå de følelser og overvejelser respondenterne gør sig.(Daugaard, 2018, S.22, MMD brugertest 10102018). 
            Der findes nogle forskellige metoder hertil f.eks. interviews og observationer. I forbindelse med interviews vil man stille mange ”hv-spørgsmål” for at opnå en større forståelse. Ofte vil besvarelsen man får tilbage, være i form af lange interviews og samtaler med en eller flere personer.</p> 
            
            <p class="tekst"> Selve databearbejdningen består primært af at fortolke og analysere de svar man får tilbage fra interviews og samtaler. Fordelen ved denne metode er, at det giver et meget dybere og nuanceret billede af en problemstilling.
            </p> 
            
        </div>  <!--Kvalitativ metode slut-->     
            
        <div id="kvantitativ"> <!--Kvantitativ start-->
            <h2> Kvantitativ </h2> 
            
            <p class="tekst"> I den kvantitative metode arbejder man med konkret og målbart data. Det vil sige data, hvor det er muligt at sætte tal på. Det kunne f.eks. være spørgeskemaer, meningsmålinger osv. Her kan vi statistisk måle og veje vores data.  
            Når vi anvender den kvantitative metode, handler det ikke om at forstå, hvorfor folk mener, som de mener. Vi er dybest set ligeglade med om vi forstår deres overvejelser, det handler om at vi kan forklare dem.</p> 
            
            <p class="tekst"> Den kvantitative metode anvendes typisk ved større undersøgelser, hvor den kvalitative metode typisk anvendes ved mindre, men dybere undersøgelser. 
            </p> 
              
            <p class="tekst">Fordelen ved den kvantitative metode er, at vi kan generalisere ud fra vores resultater. Den data man behandler, vil typisk være besvarelser af spørgeskemaer, eller notater fra observationer – Igen konkrete og målbare resultater.(Daugaard, 2018, S.24, MMD brugertest 10102018).</p> 
                    
